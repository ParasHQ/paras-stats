export default async function collectionsAPI(req, res) {
  if (process.env.NODE_ENV === 'development') {
    return res.status(200).json(DUMMY)
  }
  if (!db.ready) {
    await db.init()
  }

  const respNearMarketInfo = await axios.get(
    `https://api.coingecko.com/api/v3/coins/near/market_chart?vs_currency=usd&days=30&interval=daily`
  )

  const prices = respNearMarketInfo.data.prices.map((x) => {
    const d = new Date(x[0])
    return {
      date: `${d.getFullYear()}-${d.getMonth() + 1}-${d.getDate()}`,
      price: x[1],
    }
  })

  const x = await db.root
    .collection('activities')
    .aggregate([
      {
        $match: {
          $or: [
            {
              type: 'nft_transfer',
              price: {
                $ne: null,
              },
            },
            {
              type: 'resolve_purchase',
            },
          ],
        },
      },
      {
        $lookup: {
          from: 'tokens',
          localField: 'token_id',
          foreignField: 'token_id',
          as: 'token',
        },
      },
      {
        $set: {
          token: {
            $arrayElemAt: ['$token', 0],
          },
        },
      },
      {
        $lookup: {
          from: 'profiles',
          localField: 'token.metadata.creator_id',
          foreignField: 'accountId',
          as: 'creator',
        },
      },
      {
        $set: {
          creator: {
            $arrayElemAt: ['$creator', 0],
          },
        },
      },
      {
        $match: {
          'creator.isCreator': true,
        },
      },
    ])
    .toArray()

  console.log(x.length)

  const y = x.reduce((a, b) => {
    if (!b.token) {
      console.log(b)
    }
    const key = b.token.metadata.collection_id || b.token.contract_id

    const d = new Date(b.issued_at)
    b.date = `${d.getFullYear()}-${d.getMonth() + 1}-${d.getDate()}`
    b.dateTs = new Date(b.date).getTime()

    const hasUsdPrice = prices.find((x) => x.date === b.date)
    const usdPrice = hasUsdPrice
      ? hasUsdPrice.price
      : prices[prices.length - 1].price

    if (a[key]) {
      const price = JSBI.BigInt(b.price.toString())
      const currentVolume = JSBI.BigInt(a[key].volume || '0')
      const newVolume = JSBI.add(currentVolume, price).toString()
      a[key].volume = newVolume
      a[key].volume_usd = (newVolume / 10 ** 24) * usdPrice
      a[key].owner_ids.push(b.token.owner_id)
      a[key].total_sales += 1
    } else {
      a[key] = {
        volume: b.price.toString(),
        volume_usd: (b.price.toString() / 10 ** 24) * usdPrice,
        // collection: b.collection,
        collection_id: key,
        owner_ids: [b.token.owner_id],
        total_sales: 1,
      }
    }
    return a
  }, {})

  const sortedData = Object.keys(y)
    .map((x) => {
      const data = y[x]
      data.owner_count = [...new Set(data.owner_ids.filter((x) => x))].length
      data.avg_price = JSBI.divide(
        JSBI.BigInt(data.volume),
        JSBI.BigInt(data.total_sales)
      ).toString()
      data.avg_price_usd = data.volume_usd / data.total_sales
      delete data.owner_ids
      return data
    })
    .sort((a, b) => b.volume_usd - a.volume_usd)

  res.status(200).json(sortedData)
}
